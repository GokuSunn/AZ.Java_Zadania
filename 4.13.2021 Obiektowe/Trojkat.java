class Trojkat {
    /*
    * bylo kilka konstroktowo rozne trojkaty
    */

    public Trojkat(int wysokosc, int podstawa)
    {
        System.out.println(wysokosc*podstawa/2);
    }

    public Trojkat2(int a,int h)
    {
        System.out.println("Pole trojkata: " + (a*h/2));
    }   
}